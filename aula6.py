#Função
def soma(a, b):
    return f'{a} + {b} = {a + b}'

def subtracao(a, b):
    return f'{a} - {b} = {a - b}'

def multiplicacao(a, b):
    return f'{a} * {b} = {a * b}'

def divisao(a, b):
    return f'{a} / {b} = {a / b}'

print(soma(4, 3))
print(subtracao(4, 3))
print(multiplicacao(4, 3))
print(divisao(8, 4))